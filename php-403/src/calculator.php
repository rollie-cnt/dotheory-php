<?php

class Calculator
{
    public function add($x, $y)
    {
        return $x + $y;
    }
    public function substract($x, $y)
    {
        return $x - $y;
    }
    public function multiply($x, $y)
    {
        return $x * $y;
    }
    public function divide($x, $y)
    {
        return $x / $y;
    }
}
